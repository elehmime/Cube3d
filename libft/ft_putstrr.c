#include "ft_printf.h"

int	ft_putstrr(char	*str)
{
	int	i;

	i = 0;
	if (!str)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (str[i])
		write (1, &str[i++], 1);
	return (i);
}
