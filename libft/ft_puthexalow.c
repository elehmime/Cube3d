#include "ft_printf.h"

void	ft_puthexalow(unsigned int n, int *i)
{
	if (n >= 16)
		ft_puthexalow(n / 16, i);
	*i += 1;
	write(1, &"0123456789abcdef"[n % 16], 1);
}
