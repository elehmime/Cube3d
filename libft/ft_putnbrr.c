#include "ft_printf.h"

void	ft_putnbrr(int n, int *i)
{
	long	nb;

	nb = n;
	if (n < 0)
	{
		write (1, "-", 1);
		nb = -nb;
		*i += 1;
	}
	if (nb > 9)
	{
		ft_putnbrr(nb / 10, i);
	}
	*i += 1;
	write(1, &"0123456789"[nb % 10], 1);
}
